# 문자열 연산
print("안녕" + "하세요")

# 만약 "안녕"과 "하세요" 사이에 공백을 1게 넣고 싶다면
# 방법 1) , 사용
print("안녕", "하세요")

# 방법 2) 안녕뒤에 공백 추가
print("안녕 " + "하세요")

# 방법 3) 공백만 있는 문자열 더하기
print("안녕" + " " + "하세요")


# 문자열 곱하기
print("안녕" * 5)  # 문자열에 연산자를 사용할 경우 모두 이어져서 나옴
